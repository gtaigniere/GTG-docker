        <h1>UML</h1>

        <p>Concept objet : Ensemble d'attributs (caractéristiques), d'état significatif, de comportement.</p>
        <p>Classe : Ensemble d'objets de même type Ensemble Attributs.</p>
        <p>Objet : Instance d'une seule classe.</p>
        <p>Classe abstraite : Classe sans instance (Ne peut pas créer d'objet).</p>
        <p class="indent1fois">Une classe fille d'une classe abstraite ne peut pas invoquer de méthode de la classe parente.</p>
        <p>Encapsulation : Passer par les opérations pour agir sur les attributs.</p>
        <p>Polymorphisme : Héritage d'opérations.</p>
        <p>Méthodes d'instance : Agissent sur un seul objet.</p>
        <p>Méthodes de classe : Agissent sur plusieurs objets mais pas sur un seul.</p>
        <p>Portée des méthodes :</p>
        <ul class="indent1fois">
            <li>Privée : Accessible par les membres de la classe seulement.</li>
            <li>Protégée : Accessible par les membres de la classe et des classes dérivées.</li>
            <li>Publique : Accessible par tous.</li>
        </ul>
        <p>Package : Ensemble de classes couvrant le même domaine.</p>
